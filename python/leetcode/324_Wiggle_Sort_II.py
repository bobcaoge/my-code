class Solution(object):
    def wiggleSort(self, nums):
        """
        :type nums: List[int]
        :rtype: None Do not return anything, modify nums in-place instead.
        """
        length = len(nums)
        if length <= 1:
            return nums




def main():
    s = Solution()



if __name__ == "__main__":
    main()
